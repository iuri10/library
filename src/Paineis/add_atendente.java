    private void btn_adicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_adicionarActionPerformed

        try {
            if (txtcpf.getText().isEmpty() || txtidade.getText().isEmpty() || txtnome.getText().isEmpty() || txtsenha.getText().isEmpty() || txttelefone.getText().isEmpty() || txtuser.getText().isEmpty()) {
                IllegalArgumentException erro = new IllegalArgumentException();
                throw erro;
            } else {
                Funcionario f = new Funcionario();
                f.setIdFuncionario(null);
                f.setNome(txtnome.getText());
                f.setCpf(Integer.parseInt(txtcpf.getText()));
                f.setIdade(Integer.parseInt(txtidade.getText()));
                f.setTelefone(Integer.parseInt(txttelefone.getText()));
                f.setTipo(jComboBox1.getSelectedItem().toString());
                f.setUser(txtuser.getText());
                f.setPass(Integer.parseInt(txtsenha.getText()));

                String nome = jComboBox2.getSelectedItem().toString();
                Biblioteca b = new Biblioteca();
                int id = buscaidbiblioteca(nome);
                b.setIdBiblioteca(id);
                f.setIdBiblioteca(b);

                JOptionPane.showMessageDialog(null, "Funcionario inserido com sucesso");
                funcionarioJpa.create(f);

                limpar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro, Ha campos em branco");
        }
        atualizarTabela();
    }//GEN-LAST:event_btn_adicionarActionPerformed


