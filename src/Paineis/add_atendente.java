    private void populajcombobox2() {
        try {
            bibliotecajpa = new BibliotecaJpaController(emf);

            List<Biblioteca> list = bibliotecajpa.findBibliotecaEntities();

            for (Biblioteca biblio : list) {
                jComboBox2.addItem(biblio.getNome());
            }

        } catch (Exception e) {
        }
    }

    public void atualizarTabela() {
        List<Funcionario> lista = null;
        funcionarioJpa = new FuncionarioJpaController(emf);
        lista = funcionarioJpa.findFuncionarioEntities();
        String[] col = {"IdFuncionario", "Nome", "CPF", "Idade", "Telefone", "Tipo", "User", "Pass", "IdBiblioteca"};
        DefaultTableModel modelo = new DefaultTableModel(col, 0);
        for (Funcionario p : lista) {
            modelo.addRow(new String[]{String.valueOf(p.getIdFuncionario()), p.getNome(), String.valueOf(p.getCpf()), String.valueOf(p.getIdade()), String.valueOf(p.getTelefone()), p.getTipo(), p.getUser(), String.valueOf(p.getPass()), String.valueOf(p.getIdBiblioteca().getIdBiblioteca())});
        }
        Tabela.setModel(modelo);

    }

    private int buscaidbiblioteca(String nome) {
        List<Biblioteca> list = null;
        int id = 0;
        try {
            bibliotecajpa = new BibliotecaJpaController(emf);
            list = bibliotecajpa.buscaID(nome);
            for (Biblioteca biblio : list) {
                id = biblio.getIdBiblioteca();
            }
        } catch (Exception e) {

        }
        return id;
    }

    public void limpar() {
        txtcpf.setText("");
        txtidade.setText("");
        txtnome.setText("");
        txtsenha.setText("");
        txttelefone.setText("");
        txtuser.setText("");
    }

